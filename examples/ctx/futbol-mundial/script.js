const stageFilter = document.querySelector("#stage-filter");
const hostFilter = document.querySelector("#host-filter");
const searchInput = document.querySelector("#search");
const themeSelect = document.querySelector("#theme-select");
const langButtons = document.querySelectorAll(".lang-button");
const matchesGrid = document.querySelector("#matches-grid");
const template = document.querySelector("#match-card-template");
const resultsCount = document.querySelector("#results-count");
const heroMetrics = document.querySelector("#hero-metrics");
const root = document.documentElement;

let currentLanguage = "es";

const unique = (items) => [...new Set(items)];

function t(key) {
  return translations[currentLanguage][key] || translations.es[key] || key;
}

function resolveMatchText(match, field) {
  const keyField = `${field}Key`;
  return match[keyField] ? t(match[keyField]) : match[field];
}

function populateSelect(select, values, formatter) {
  const allOption = select.querySelector('option[value="all"]');
  select.innerHTML = "";
  select.appendChild(allOption);
  values.forEach((value) => {
    const option = document.createElement("option");
    option.value = value;
    option.textContent = formatter(value);
    select.appendChild(option);
  });
}

function renderMetrics() {
  heroMetrics.innerHTML = "";

  const metrics = [
    { label: t("metricMatches"), value: matches.length },
    { label: t("metricVenues"), value: unique(matches.map((match) => match.stadium)).length },
    { label: t("metricStages"), value: unique(matches.map((match) => match.stageKey)).length },
    { label: t("metricCountries"), value: unique(matches.map((match) => match.hostKey)).length }
  ];

  metrics.forEach((metric) => {
    const card = document.createElement("div");
    card.className = "metric-card";
    card.innerHTML = `<strong>${metric.value}</strong><span>${metric.label}</span>`;
    heroMetrics.appendChild(card);
  });
}

function badgeText(type) {
  if (type === "confirmed") return t("badgeConfirmed");
  if (type === "knockout") return t("badgeKnockout");
  return t("badgeSlot");
}

function badgeClass(type) {
  if (type === "confirmed") return "badge-confirmed";
  if (type === "knockout") return "badge-knockout";
  return "badge-slot";
}

function renderMatches(list) {
  matchesGrid.innerHTML = "";

  if (!list.length) {
    const empty = document.createElement("div");
    empty.className = "empty-state";
    empty.textContent = t("emptyState");
    matchesGrid.appendChild(empty);
    resultsCount.textContent = t("resultCount").replace("{count}", "0");
    return;
  }

  const fragment = document.createDocumentFragment();

  list.forEach((match) => {
    const node = template.content.cloneNode(true);
    node.querySelector(".match-number").textContent = match.number;
    node.querySelector(".match-badge").textContent = badgeText(match.type);
    node.querySelector(".match-badge").classList.add(badgeClass(match.type));
    node.querySelector(".match-stage").textContent = t(match.stageKey);
    node.querySelector(".home-team").textContent = resolveMatchText(match, "home");
    node.querySelector(".away-team").textContent = resolveMatchText(match, "away");
    node.querySelector(".match-date").textContent = match.date;
    node.querySelector(".match-venue").textContent = `${match.city} - ${match.stadium} - ${t(match.hostKey)}`;
    node.querySelector(".match-note").textContent = t(match.noteKey);
    fragment.appendChild(node);
  });

  matchesGrid.appendChild(fragment);
  resultsCount.textContent = t("resultCount").replace("{count}", String(list.length));
}

function filterMatches() {
  const search = searchInput.value.trim().toLowerCase();
  const stage = stageFilter.value;
  const host = hostFilter.value;

  const filtered = matches.filter((match) => {
    const haystack = [
      match.number,
      t(match.stageKey),
      t(match.hostKey),
      match.city,
      match.stadium,
      resolveMatchText(match, "home"),
      resolveMatchText(match, "away"),
      t(match.noteKey)
    ].join(" ").toLowerCase();

    const matchesSearch = !search || haystack.includes(search);
    const matchesStage = stage === "all" || match.stageKey === stage;
    const matchesHost = host === "all" || match.hostKey === host;

    return matchesSearch && matchesStage && matchesHost;
  });

  renderMatches(filtered);
}

function applyTheme(theme) {
  root.setAttribute("data-theme", theme);
  if (themeSelect) {
    themeSelect.value = theme;
  }
  localStorage.setItem("fixture-theme", theme);
}

function restoreTheme() {
  const savedTheme = localStorage.getItem("fixture-theme") || "arena";
  applyTheme(savedTheme);
}

function translateStaticContent() {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = t(element.dataset.i18n);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    element.placeholder = t(element.dataset.i18nPlaceholder);
  });

  document.querySelectorAll("[data-i18n-option]").forEach((option) => {
    option.textContent = t(option.dataset.i18nOption);
  });

  const allStagesOption = stageFilter.querySelector('option[value="all"]');
  const allHostsOption = hostFilter.querySelector('option[value="all"]');
  allStagesOption.textContent = t("allStages");
  allHostsOption.textContent = t("allHosts");
}

function renderLocalizedControls() {
  const previousStage = stageFilter.value || "all";
  const previousHost = hostFilter.value || "all";

  populateSelect(stageFilter, unique(matches.map((match) => match.stageKey)), (value) => t(value));
  populateSelect(hostFilter, unique(matches.map((match) => match.hostKey)), (value) => t(value));

  stageFilter.value = unique(["all", ...matches.map((match) => match.stageKey)]).includes(previousStage) ? previousStage : "all";
  hostFilter.value = unique(["all", ...matches.map((match) => match.hostKey)]).includes(previousHost) ? previousHost : "all";
}

function applyLanguage(language) {
  currentLanguage = language;
  document.documentElement.lang = language === "zh" ? "zh-CN" : language;
  localStorage.setItem("fixture-language", language);

  langButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === language);
  });

  translateStaticContent();
  renderLocalizedControls();
  renderMetrics();
  filterMatches();
}

function restoreLanguage() {
  const savedLanguage = localStorage.getItem("fixture-language") || "es";
  applyLanguage(savedLanguage);
}

restoreTheme();
restoreLanguage();

stageFilter.addEventListener("change", filterMatches);
hostFilter.addEventListener("change", filterMatches);
searchInput.addEventListener("input", filterMatches);

if (themeSelect) {
  themeSelect.addEventListener("change", (event) => applyTheme(event.target.value));
}

langButtons.forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang));
});
